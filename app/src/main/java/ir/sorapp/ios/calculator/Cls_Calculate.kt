package ir.sorapp.ios.calculator

fun Add(a:Float,b:Float):Float
{
    return a + b;
}

fun Sub(a:Float,b:Float):Float
{
    return a - b;
}

fun Mul(a:Float,b:Float):Float
{
    return a * b;
}

fun Div(a:Float,b:Float):Float
{
    return a / b;
}

fun re_Div(a:Float,b:Float):Float
{
    return a % b;
}